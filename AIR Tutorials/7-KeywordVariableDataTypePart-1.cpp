#include <iostream>
using namespace std;

int main()
{
    // Variable Declaration
    int num1, num2; // Declaring two integer variables
    float num3;     // Declaring a float variable
    double num4;    // Declaring a double variable
    char ch;        // Declaring a character variable

    // Variable Initialization
    num1 = 3;         // Initializing num1 with the value 3
    num2 = 4;         // Initializing num2 with the value 4
    num3 = 7.98;      // Initializing num3 with the value 7.98
    num4 = 7.6546363; // Initializing num4 with the value 7.6546363
    ch = 'E';         // Initializing ch with the character 'E'

    // Variable can be Declared & Initialized at a time

    return 0; // Indicating successful program termination
}
