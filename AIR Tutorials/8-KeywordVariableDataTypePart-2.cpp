#include <iostream>
using namespace std;

int main()
{
    // Initializing and displaying integer variables
    int num1 = 5, num2 = 25;
    cout << "Number-1 is: " << num1 << endl
         << "Number-2 is: " << num2 << endl;
    
    // Initializing and displaying float and double variables
    float num3 = 4.09;
    double num4 = 5.234456;
    cout << "Floating number is: " << num3 << endl;
    cout << "Double number is: " << num4 << endl;
    
    // Initializing and displaying a character variable
    char ch = 'E';
    cout << "Character is: " << ch << endl;
    
    return 0; // Indicating successful program termination
}
